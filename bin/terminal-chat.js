#!/usr/bin/env node

/**
 * terminal-chat.js  (bin entry-point)
 * ------------------------------------
 * Main CLI entry-point exposed as the `terminal-chat` command.
 * Presents a simple numbered menu that lets the user:
 *   [1] Start a TLS chat server in the background
 *   [2] Connect to an existing chat server as a client
 *   [3] Stop a previously started background server
 *
 * Server processes are tracked by PID files stored under
 * ~/.terminal-chat/pids/server-<port>.pid so they can be
 * cleanly shut down without a process manager.
 */

const { spawn, spawnSync } = require('child_process');
const fs = require('fs');
const path = require('path');
const os = require('os');
const readline = require('readline');

// ── Paths ───────────────────────────────────────────────────────────────────

// Absolute paths to the server and client scripts inside lib/
const serverPath = path.join(__dirname, '../lib/server.js');
const clientPath = path.join(__dirname, '../lib/client.js');

// Directory that holds one PID file per running server instance
const pidDir = path.join(os.homedir(), '.terminal-chat', 'pids');
if (!fs.existsSync(pidDir)) fs.mkdirSync(pidDir, { recursive: true });

// TLS certificate files generated by postinstall.js (or on-demand below)
const keyPath = path.join(__dirname, '../lib/server.key');
const crtPath = path.join(__dirname, '../lib/server.crt');

// ── Welcome banner ──────────────────────────────────────────────────────────
console.log(`   ,_,        TERMINAL CHAT 🔐\n  (O,O)\n  (   )\n   " "\n`);

// ── Readline interface ──────────────────────────────────────────────────────
const rl = readline.createInterface({ input: process.stdin, output: process.stdout, prompt: '> ' });

// Display the main menu options to the user
console.log('Select what you want:');
console.log('[1] Create chat server');
console.log('[2] Connect to chat');
console.log('[3] Stop chat server');

// Read the user's menu choice
rl.question('> ', choice => {
  choice = choice.trim();

  // ── Option 1: Start a background TLS chat server ──────────────────────────
  if (choice === '1') {
    rl.question('Enter port to run chat server (default 1599): ', portInput => {
      // Default to port 1599 if the user pressed Enter without typing a number
      const port = portInput.trim() ? parseInt(portInput.trim(), 10) : 1599;

      // Path for the PID file that tracks this server's process ID
      const pidFile = path.join(pidDir, `server-${port}.pid`);

      // Generate a self-signed TLS certificate on-the-fly if it's missing.
      // Normally postinstall.js already handles this, but this is a safety net.
      if (!fs.existsSync(keyPath) || !fs.existsSync(crtPath)) {
        console.log('🔐 TLS certificate not found. Generating...');
        const opensslCmd = `openssl req -newkey rsa:2048 -nodes -keyout "${keyPath}" -x509 -days 365 -out "${crtPath}" -subj "/CN=localhost"`;
        const result = spawnSync(opensslCmd, { shell: true, stdio: 'inherit' });
        if (result.error) {
          console.error('Error generating TLS certificate:', result.error);
          process.exit(1);
        }
      }

      // Spawn server.js as a fully detached background process.
      // `detached: true` + `child.unref()` means the parent CLI can exit
      // without killing the server, and the server keeps running.
      const child = spawn(process.execPath, [serverPath, String(port)], {
        detached: true,
        stdio: 'ignore'         // don't inherit stdin/stdout so it truly backgrounds
      });
      child.unref();            // allow this (parent) process to exit independently

      // Persist the PID so option [3] can kill the server later
      fs.writeFileSync(pidFile, child.pid.toString(), 'utf-8');
      console.log(`Server started in background on port ${port} (PID ${child.pid}).`);
      rl.close();
    });

  // ── Option 2: Connect to a remote chat server as a client ─────────────────
  } else if (choice === '2') {
    rl.question('Enter server IP: ', host => {
      rl.question('Enter server port: ', portInput => {
        rl.question('Enter your name: ', name => {
          const port = portInput.trim();

          // Generate a short, random 4-character user ID (e.g. "AB47").
          // This ID is used for private messages (/pm) and presence announcements.
          const userId =
            String.fromCharCode(65 + Math.floor(Math.random() * 26)) + // random A-Z
            String.fromCharCode(65 + Math.floor(Math.random() * 26)) + // random A-Z
            Math.floor(Math.random() * 10) +                            // random 0-9
            Math.floor(Math.random() * 10);                             // random 0-9

          // Close readline BEFORE spawning client.js.
          // If readline still owns stdin when the child process tries to use it,
          // keyboard input won't reach the child.
          rl.close();

          // Spawn client.js with stdio: 'inherit' so the child shares the
          // terminal directly — keystrokes go to the client, output appears here.
          // Connection details are passed through environment variables instead of
          // argv so they don't appear in `ps` output.
          const child = spawn(process.execPath, [clientPath], {
            stdio: 'inherit',
            env: {
              ...process.env,
              CHAT_HOST: host,
              CHAT_PORT: port,
              CHAT_NAME: name,
              CHAT_ID:   userId
            }
          });

          // Keep the parent process alive until the client exits,
          // then forward the exit code so the shell sees the right status.
          child.on('exit',  code  => { process.exit(code || 0); });
          child.on('error', err   => { console.error('Failed to start client:', err.message); process.exit(1); });
        });
      });
    });

  // ── Option 3: Stop a running background server ────────────────────────────
  } else if (choice === '3') {
    rl.question('Enter port of server to stop (default 1599): ', portInput => {
      const port = portInput.trim() ? parseInt(portInput.trim(), 10) : 1599;
      const pidFile = path.join(pidDir, `server-${port}.pid`);

      // If no PID file exists, there's no tracked server on that port
      if (!fs.existsSync(pidFile)) {
        console.log(`No running server found on port ${port}.`);
        rl.close();
        return;
      }

      const pid = parseInt(fs.readFileSync(pidFile, 'utf-8'), 10);

      try {
        // Send SIGTERM to gracefully stop the server process
        process.kill(pid);
        // Remove the PID file so stale entries don't accumulate
        fs.unlinkSync(pidFile);
        console.log(`Server on port ${port} (PID ${pid}) stopped.`);
      } catch (err) {
        // process.kill throws if the PID doesn't exist (already dead)
        console.error('Error stopping server:', err.message);
      }

      rl.close();
    });

  // ── Invalid choice ─────────────────────────────────────────────────────────
  } else {
    console.log('Invalid choice. Exiting.');
    rl.close();
  }
});
